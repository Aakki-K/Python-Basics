'''Write a program that works out whether if a given number
 is an odd or even number.'''

'''
Input = 11
Output = odd
'''

# TODO 1: Welcome Code
print(f"***** Welcome to Odd or Even display AI")

# TODO 2: Taking input from user
Number = int(input("Enter the number (Don't input Decimal numbers) : "))

# TODO 3: Logic & Output:
if Number % 2 == 0:
    print(f'{Number} is "Even Number"')
else:
    print(f'{Number} is "Odd Number"')

